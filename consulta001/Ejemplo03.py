# Ejemplo de entrada por teclado
# Programa que permite ingresar e imprimir los datos personales de una persona

print("Ingrese su nombre:")
nombre = input()
print("Ingrese su apellido:")
apellido = input()
print("Ingrese su edad:")
edad = int(input())
print("Ingrese su estatura:")
estatura = float(input())

print("Sus datos personales son:\n","Nombre completo: ", nombre,"",
      apellido, "\n","Edad: ", edad , "\n","Estatura: ", estatura)